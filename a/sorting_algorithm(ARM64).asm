.data
    msg: .asciz "Hello, World!\n"  // Строка для вывода с переводом строки

.text
.globl _start

_start:
    // Вывод строки на экран
    mov x0, 1             // Файловый дескриптор (stdout)
    adr x1, msg           // Адрес строки
    mov x2, 14            // Длина строки
    mov x16, 0x1          // Системный вызов для вывода
    svc 0x80              // Вызов ядра

    // Выход из программы
    mov x0, 0             // Код возврата
    mov x16, 0x1          // Системный вызов для выхода
    svc 0x80              // Вызов ядра
