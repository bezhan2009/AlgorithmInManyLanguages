.data
    msg: .asciz "Hello, World!\n"  // Строка для вывода с переводом строки

.text
.globl _start

_start:
    // Вывод строки на экран
    mov r0, #1             // Файловый дескриптор (stdout)
    adr r1, msg            // Адрес строки
    mov r2, #14            // Длина строки
    mov r7, #4             // Системный вызов для вывода
    swi 0x80               // Вызов ядра

    // Выход из программы
    mov r0, #0             // Код возврата
    mov r7, #1             // Системный вызов для выхода
    swi 0x80               // Вызов ядра
