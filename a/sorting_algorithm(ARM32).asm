.section .data
array:  .word 1, 2, 3, 4, 5  // Массив данных
length: .word 5             // Длина массива

.section .text
.global _start

_start:
    ldr r0, =array       // Загрузка адреса массива в r0
    ldr r1, =length      // Загрузка адреса длины массива в r1
    ldr r1, [r1]         // Загрузка длины массива в r1
    mov r2, #0           // Инициализация суммы в r2

loop:
    cmp r1, #0           // Проверка длины массива
    beq end_loop         // Если длина 0, перейти к завершению
    ldr r3, [r0], #4     // Загрузка текущего элемента и инкремент адреса
    add r2, r2, r3       // Добавление текущего элемента к сумме
    sub r1, r1, #1       // Уменьшение длины массива на 1
    b loop               // Переход к началу цикла

end_loop:
    mov r7, #1           // Системный вызов для выхода
    mov r0, #0           // Код возврата 0
    svc #0               // Вызов системного вызова
